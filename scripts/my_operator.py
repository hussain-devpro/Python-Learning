# Arithmetic Operator

print(7 + 5)  # 7 + 5 = 12
print(7 - 5)  # 7 - 5 = 2
print(7 * 5)  # 7 * 5 = 35
print(7 / 5)  # 7 / 5 = 1.4
print(7 % 5)  # 7 % 5 = 2 # Remainder
print(7 // 5)  # 7 // 5 = 1 # Integer Division
print(7 ** 5)  # 7 ** 5 = 16807 # Exponent

# Logical Operator

print(7 == 5)  # 7 == 5 : False
print(7 != 5)  # 7 != 5 : True
print(7 < 5)  # 7 < 5 : False
print(7 > 5)  # 7 > 5 : True
print(7 >= 5)  # 7 >= 5 : True
print(7 <= 5)  # 7 <= 5 : False

